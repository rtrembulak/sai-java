public class Variables {
    public static void main(String args[]) {
        byte abc = 127; // update value to 128. Or -128, // assign a character literal.
        System.out.println(abc);

        int e = (int)2.5; // update value to Integer.MAX_VALUE + 1
        System.out.println(e);

        char c = 'c'; // assign an integer value
        System.out.println(c);

        int example = 'c';
        System.out.println(example);

        long l = Long.MAX_VALUE +2;
        System.out.println(l);

        float a = 0.1f; // increase decimal precision to 16 digits.
        System.out.println(a);

        double d = 0.1d; // increase decimal precision to 16 digits.
        System.out.println(d);
    }
}
